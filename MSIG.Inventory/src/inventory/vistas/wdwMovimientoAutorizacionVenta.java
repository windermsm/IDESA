    public void totalFactura() {

        double total_factura = 0;
        
        for (int c = 0; c < detalle_nuevo.getRowCount(); c++) {
            try {
                total_factura = total_factura + Float.parseFloat(detalle_nuevo.getValueAt(c, 8).toString());
            } catch (Exception error) {
                total_factura = total_factura + 0;
            }
        }
        
        double iva = matematica.aproxima(total_factura * 0.12, 2);
        double total = matematica.redondear(total_factura);
        
        System.out.println("Total: " + total);
        System.out.println("IVA: " + iva);
        
        wdwMovimientoVentaDeProductosNueva.txtTotalFactura.setText(String.valueOf(total));
        wdwMovimientoVentaDeProductosNueva.txtIva.setText(String.valueOf(iva));

    }

