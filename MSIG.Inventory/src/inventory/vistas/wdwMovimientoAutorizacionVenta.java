    private void btnAutorizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAutorizarActionPerformed
        
        //Consultar los datos del producto
        AccesoProducto acceso_producto = new AccesoProducto();
        AccesoUsuario acceso_usuario = new AccesoUsuario();
        ObjetosProducto producto = new ObjetosProducto();
        
        String tipo = "";
        
        try { 
            producto = acceso_producto.buscarInformacionProducto(txtIdProducto.getText(), Inventory.lblUsuario.getText(), Inventory.lblTerminal.getText());
        } catch (Exception Error) {
            mensaje.manipulacionExcepciones("critico", "Ocurrrio un error al buscar informacion del Producto.", "Buscar Producto");
        }
        
        try { 
            tipo = acceso_usuario.retornaTipoUsuario(txtUsuario.getText(), txtContrasenia.getText(), Inventory.lblUsuario.getText(), Inventory.lblTerminal.getText());
        } catch (Exception Error) {
            mensaje.manipulacionExcepciones("critico", "Ocurrrio un error al buscar informacion del Producto.", "Buscar Producto");
        }
        
        if(tipo.equals("Administrador")) {
            
            Float cantidad = Float.parseFloat(txtCantidad.getText());
            double precio = Double.parseDouble(txtPrecio.getText());
            double subtotal = cantidad * precio;
            
            Object[] datos = new Object[12];
            try { datos[0] = detalle_nuevo.getRowCount() + 1; } catch (Exception Error) { mensaje.manipulacionExcepciones("critico", "Fila Seleccionada \nError: " + Error.getMessage(), "Dato No. " + detalle_nuevo.getRowCount() + 1); }
            try { datos[1] = txtIdProducto.getText(); } catch (Exception Error) { mensaje.manipulacionExcepciones("critico", "ID Producto \nError: " + Error.getMessage(), "Dato No. " + detalle_nuevo.getRowCount() + 1); }
            try { datos[2] = txtDescripcion.getText(); } catch (Exception Error) { mensaje.manipulacionExcepciones("critico", "Descripcion \nError: " + Error.getMessage(), "Dato No. " + detalle_nuevo.getRowCount() + 1); }
            try { datos[3] = cantidad; } catch (Exception Error) { mensaje.manipulacionExcepciones("critico", "Cantidad Venta \nError: " + Error.getMessage(), "Dato No. " + detalle_nuevo.getRowCount() + 1); }
            try { datos[4] = Float.parseFloat(txtPrecioCompra.getText()); } catch (Exception Error) { mensaje.manipulacionExcepciones("critico", "Precio Compra \nError: " + Error.getMessage(), "Dato No. " + detalle_nuevo.getRowCount() + 1); }
            try { datos[5] = matematica.aproxima(precio, 2); } catch (Exception Error) { mensaje.manipulacionExcepciones("critico", "Precio Descuento \nError: " + Error.getMessage(), "Dato No. " + detalle_nuevo.getRowCount() + 1); }
            try { datos[6] = Float.parseFloat(txtMinimo.getText()); } catch (Exception Error) { mensaje.manipulacionExcepciones("critico", "Precio Minimo \nError: " + Error.getMessage(), "Dato No. " + detalle_nuevo.getRowCount() + 1); }
            try { datos[7] = Float.parseFloat(txtNormal.getText()); } catch (Exception Error) { mensaje.manipulacionExcepciones("critico", "Precio Normal \nError: " + Error.getMessage(), "Dato No. " + detalle_nuevo.getRowCount() + 1); }
            try { datos[8] = subtotal; } catch (Exception Error) { mensaje.manipulacionExcepciones("critico", "Total \nError: " + Error.getMessage(), "Dato No. " + detalle_nuevo.getRowCount() + 1); }
            try { datos[9] = "S/C"; } catch (Exception Error) { mensaje.manipulacionExcepciones("critico", "Comentario \nError: " + Error.getMessage(), "Dato No. " + detalle_nuevo.getRowCount() + 1); }
            try { datos[10] = 0.0; } catch (Exception Error) { mensaje.manipulacionExcepciones("critico", "Arancel \nError: " + Error.getMessage(), "Dato No. " + detalle_nuevo.getRowCount() + 1); }
            try { datos[11] = "NO"; } catch (Exception Error) { mensaje.manipulacionExcepciones("critico", "Guardado \nError: " + Error.getMessage(), "Dato No. " + detalle_nuevo.getRowCount() + 1); }
            
            try {
                detalle_nuevo.addRow(datos);
                wdwMovimientoVentaDeProductosNueva.tblDetalleFactura.setModel(detalle_nuevo); 
            } catch (Exception Error) {
                System.out.println("Error: " + Error.toString());
            }

            totalFactura();
            this.dispose();

            
        } else {
            mensaje.manipulacionExcepciones("critico", "Usted no puede autorizar precios.", "Autoriza Precio");
            txtUsuario.setText("");
            txtContrasenia.setText("");
        }
        
        
        
    }//GEN-LAST:event_btnAutorizarActionPerformed

