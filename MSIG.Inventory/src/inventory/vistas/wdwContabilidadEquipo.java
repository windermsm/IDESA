    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        int id_guardado = txtId.getText().isEmpty() ? 0 : Integer.parseInt(txtId.getText());
        
        if ( id_guardado > 0) {
            mensaje.manipulacionExcepciones("critico", "Esto ya fue guardado con aterioridad.", "Guardar Equipo");
        } else {

            equipo.setDescripcion_equipo(txtDescripcion.getText());
            equipo.setDescripcion_equipo(txtDepreciacion.getText());
            String serie = txtSerie.getText().trim().replace("'", "´").toUpperCase();
            equipo.setNumero_serie(serie);
            equipo.setCosto_equipo(Float.parseFloat(txtCosto.getText()));
            equipo.setEstado_equipo(cbxEstado.getSelectedItem().toString());

            try {
                int id = acceso.insertarEquipo(equipo, Inventory.lblUsuario.getText(), Inventory.lblTerminal.getText());
                if (id > 0) {
                    txtId.setText(String.valueOf(id));
                    String resultado_actualizacion = acceso.actualizarSerieEquipo(serie, Inventory.lblUsuario.getText(), Inventory.lblTerminal.getText());
                    if (resultado_actualizacion.equals("Operacion realizada con exito.")) {
                        //Consultar id del producto según la serie
                        ObjetosSerie producto = acceso.buscarNumeroSerie(serie, Inventory.lblUsuario.getText(), Inventory.lblTerminal.getText());
                        //Quitar uno del inventario para no realizar el ajuste
                        if (producto == null || producto.getId_producto().isEmpty()) {
                            mensaje.manipulacionExcepciones("critico", "No se encontro la serie tendra que hacer un ajuste para descontar el Producto del inventario.", "Descontar Producto");
                        } else {
                            mensaje.manipulacionExcepciones("informacion", acceso.actualizarCantidadProducto(producto.getId_producto(), Inventory.lblUsuario.getText(), Inventory.lblTerminal.getText()), "Descontar Produto Encontrado");
                        }
                    } else {
                        mensaje.manipulacionExcepciones("critico", "No se pudo actualizar el estado de la Serie. Por favor verficar o intentar de nuevo.", title);
                    }
                }
            } catch (Exception Error) {
                agregarLog(Error.toString());
                mensaje.manipulacionExcepciones("critico", Error.getMessage(), "Guardar Equipo");
            }
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

