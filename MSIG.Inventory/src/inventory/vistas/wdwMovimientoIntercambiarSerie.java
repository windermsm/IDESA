    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(txtIdCorrecto.getText().isEmpty() || txtIdErroneo.getText().isEmpty()) {
            mensaje.manipulacionExcepciones("informacion", "Por favor ingrese las Serie y consulte su informacion.", "Guardar Intercambio");
        } else {
            acceso.asignarVentaSerie(txtIdErroneo.getText(), txtVentaCorrecta.getText(), txtEstadoCorrecto.getText(), "Intercambio Serie", "Guardar", Inventory.lblUsuario.getText(), Inventory.lblTerminal.getText());
            //Validar si son del mismo tipo de producto
            if(txtIdProductoActual.getText().equals(txtIdProductoNuevo.getText())) {
                acceso.asignarVentaSerie(txtIdCorrecto.getText(), txtVentaErronea.getText(), txtEstadoErroneo.getText(), "Intercambio Serie", "Guardar", Inventory.lblUsuario.getText(), Inventory.lblTerminal.getText());
            } else {
                //Si son distintos cambiar el codigo de producto a la serie
                //y guardarlo en el historial de intercambios
                mensaje.manipulacionExcepciones("informacion", "El Producto elegido es distinto al Facturado", "Guardar Intercambio");
                acceso.asignarVentaSerieDistintoProducto(txtIdCorrecto.getText(), txtVentaErronea.getText(), txtEstadoErroneo.getText(), txtIdProductoActual.getText(),"Intercambio Serie", "Guardar", Inventory.lblUsuario.getText(), Inventory.lblTerminal.getText());
            }
            //Guardar informacion de auditoria del intercambio
            acceso.insertarIntercambio(incorrecto, correcto, txtMotivo.getText(), txtIdProductoActual.getText(), 
                    txtIdProductoNuevo.getText(), txtIdCompraActual.getText(), txtIdCompraNueva.getText(), "Intercambio Serie", "Guardar Intercambio", Inventory.lblUsuario.getText(), Inventory.lblTerminal.getText());
            mensaje.manipulacionExcepciones("informacion", "La Serie fue intercambiada al ID Factura " + txtVentaErronea.getText() + ".", "Guardar Intercambio");
            limpiarDatos();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        //Consulta información de la serie incorrecta
        try {
            incorrecto = acceso.buscarNumeroSerie(txtSerieErronea.getText().toUpperCase().replaceAll("'", "´"), Inventory.lblUsuario.getText(), Inventory.lblTerminal.getText());
            txtIdErroneo.setText(String.valueOf(incorrecto.getId_serie()));
            txtVentaErronea.setText(String.valueOf(incorrecto.getId_factura()));
            txtEstadoErroneo.setText(incorrecto.getEstado());
            txtDescripcionErronea.setText(incorrecto.getDescripcion());
            txtIdProductoActual.setText(incorrecto.getId_producto());
            txtIdCompraActual.setText(String.valueOf(incorrecto.getId_compra()));
        } catch (Exception Error) {
            mensaje.manipulacionExcepciones("critico", "Ocurrio un error al buscar la Serie Incorrecta.", "Buscar Serie Incorrecta");
        }
        
        //Consulta información de la serie correcta
        try {
            correcto = acceso.buscarNumeroSerie(txtSerieCorrecta.getText().toUpperCase().replaceAll("'", "´"), Inventory.lblUsuario.getText(), Inventory.lblTerminal.getText());
            txtIdCorrecto.setText(String.valueOf(correcto.getId_serie()));
            txtVentaCorrecta.setText(String.valueOf(correcto.getId_factura()));
            txtEstadoCorrecto.setText(correcto.getEstado());
            txtDescripcionCorrecta.setText(correcto.getDescripcion());
            txtIdProductoNuevo.setText(correcto.getId_producto());
            txtIdCompraNueva.setText(String.valueOf(correcto.getId_compra()));
        } catch (Exception Error) {
            mensaje.manipulacionExcepciones("critico", "Ocurrio un error al buscar la Serie Correcta.", "Buscar Serie Correcta");
        }
        
        //Vaiidación de tipos de productos elegidos
        if(!txtIdProductoActual.getText().equals(txtIdProductoNuevo.getText())) {
            mensaje.manipulacionExcepciones("interrogante", "Los Productos seleccionados no coinciden con la Factura. ¿Desea continuar?", "Verficacion de ID Producto");
            if (mensaje.valor != 0) {
                this.dispose();
            }
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

