    private void mostrarRegistrosGuardados(String pFecha_inicial , String pFecha_final){
        
        DefaultTableModel tabla = new DefaultTableModel();
        ArrayList<ObjetosProforma> listaProformas = new ArrayList();
        
        try{
            listaProformas = NuevaProforma.seleccionarProformaFecha(pFecha_inicial, pFecha_final, Inventory.lblUsuario.getText(), Inventory.lblTerminal.getText());
        }catch(Error error){
            Mensaje.manipulacionExcepciones("critico", error.getMessage(), "Seleccionar Proforma Fecha");
        }
        
        if(listaProformas.isEmpty()){
            Mensaje.manipulacionExcepciones("critico", "No existen datos almacenados de las proformas", "Mostrar Registros Guardados");
        } else {
            tabla.addColumn("ID Proforma");
            tabla.addColumn("ID Cliente");
            tabla.addColumn("ID Empleado");
            tabla.addColumn("Nombre");
            tabla.addColumn("Fecha");
            tabla.addColumn("Direccion");
            tabla.addColumn("Total");
            tabla.setRowCount(listaProformas.size());
            int cProforma = 0;
            
            for (ObjetosProforma xFactura : listaProformas) {
                tabla.setValueAt(xFactura.getId_proforma(), cProforma, 0);
                tabla.setValueAt(xFactura.getId_cliente(), cProforma, 1);
                tabla.setValueAt(xFactura.getId_empleado(), cProforma, 2);
                tabla.setValueAt(xFactura.getNombre_proforma(), cProforma, 3);
                tabla.setValueAt(xFactura.getFecha_proforma(), cProforma, 4);
                tabla.setValueAt(xFactura.getDir_envio_proforma(), cProforma, 5);
                tabla.setValueAt(xFactura.getTotal_proforma(), cProforma, 6);
                cProforma++;
            }
            
            tblDetalleVenta.setModel(tabla);
            tblDetalleVenta.setCellSelectionEnabled(false);
        }
    }
    
    
